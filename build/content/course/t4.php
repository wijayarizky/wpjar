	<br>
	<h3>Compliance to the government law and regulation.</h3>
	<p align="justify">
		Dalam proses peretasan dalam bentuk penetration test atau apapun untuk mengetahui kelemahan dari suatu system dan meningkatkan 
		keamanan dari system tersebut, seorang ethical hacker dalam melakukan ethical hacking harus mematuhi segala perundang – undangan 
		yang berlaku. Seperti di Indonesia para ethical hacker harus mematuhi UU ITE yang berlaku di Indonesia.
	</p>
	<br>
	<h3>Direct impact of security breach on corporate asset base and goodwill.</h3>
	<p align="justify">
		Para ethical hacker sangan dilarang untuk menyebarkan / membocorkan maupun merusak data maupun property dari suatu perusahaan 
		sesuai dengan elemen – elemen dari keamanan informasi.
	</p>
	<br>
	<h3>It is difficult to centralize security in a distributed computing environment.</h3>
	<p align="justify">
		Mampu untuk menemukan cara menanggulangi keamanan dalam system terdistribusi yang dimana pada dasarnya system terdistribusi 
		lebih sulit untuk dijaga dibandingkan dengan system terpusat, namun keuntungan dari system terdistribusi membuat banyak 
		perusahaan menggunakan system tersebut dan juga sebanding dengan kelebihannya system terdistribusi juga rentan terhadap 
		kebocoran informasi dan trouble dalam keamanan sehingga perlunya meningkatkan penanggulangan keamanan untuk system 
		terdistribusi.
	</p>
	<br>
	<h3>Evolution of technology focused on ease of use.</h3>
	<p align="justify">
		Sesuai dengan security, functionally and usability triangle ethical hacker dituntut untuk dapat focus pada kemudahan pengguna 
		namun dengan tingkat keamanan yang tetap ter-jaga karena berdasarkan triangle tersebut semakin meningkatnya keamanan maka 
		kemudahan pengguna serta fitur dari suatu system akan berkurang.
	</p>
	<br>
	<h3>Increased number of network-based applications.</h3>
	<p align="justify">
		Dengan meningkatnya aplikasi berbasis jaringan, kendala pada keamanan pun menjadi semakin meningkat dikarenakan pngguna dari 
		aplikasi berbasis jaringan juga semakin banyak serta banyak pembuat aplikasi yang belum mengetes keamanan dari kode program 
		yang dibuatnya maka disini ethical hacker diharapkan dapat menganalisa banyak celah yang ber-beda – beda serta menemukan 
		solusi yang tepat untuk mengatasi masalah keamanan pada setiap system tersebut.
	</p>
	<br>
	<h3>Increasing complexity of computer infrastructure administration and management.</h3>
	<p align="justify">
		Seleain mengingkatkan kemanan melalui analisa celah pada suatu system proses ethical hacking juga diharapkan dapat 
		meningkatkan keamanan melalui penambahan infrastruktur computer yang dianggap mampu untuk menanggulangi masalah tersebut 
		serta memberikan saran dalam proses management suatu infrastruktur computer dan alur proses administrasi dari system 
		tersebut.
	</p>
